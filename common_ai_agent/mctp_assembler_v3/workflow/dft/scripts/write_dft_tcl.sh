#!/usr/bin/env bash
# write_dft_tcl.sh — Generate <ip>/dft/run.tcl from SSOT dft: section.
# Args: <ip_name>
set -uo pipefail

PDK_ENV="$(cd "$(dirname "$0")/../.." && pwd -P)/scripts/pdk_env.sh"
[ -f "${PDK_ENV}" ] && source "${PDK_ENV}"

IP="${1:-}"
if [ -z "${IP}" ]; then echo "[DFT] usage: write_dft_tcl.sh <ip_name>" >&2; exit 2; fi
SSOT="${IP}/yaml/${IP}.ssot.yaml"
NETLIST="${IP}/syn/out/synth.v"
TCL="${IP}/dft/run.tcl"
OUT="${IP}/dft/out"
mkdir -p "${IP}/dft" "${OUT}"

if [ ! -f "${SSOT}" ]; then echo "[DFT] missing SSOT: ${SSOT}" >&2; exit 2; fi
if [ ! -s "${NETLIST}" ]; then echo "[DFT] missing ${NETLIST}" >&2; exit 5; fi

# Pull DFT params from SSOT.
read TOP SE_PORT MAX_CHAINS MAX_LEN < <(python3 - "${SSOT}" "${IP}" <<'PY'
import sys, pathlib
ssot, ip = sys.argv[1:3]
try:
    import yaml; d = yaml.safe_load(pathlib.Path(ssot).read_text(encoding="utf-8", errors="replace")) or {}
except Exception: d = {}
_t = d.get("top_module")
if isinstance(_t, dict): _t = _t.get("name")
top = _t or d.get("top") or ip
dft = d.get("dft", {}) or {}
print(top, dft.get("scan_enable_port") or "scan_en",
      int(dft.get("max_chains", 4)), int(dft.get("max_chain_length", 100)))
PY
)

# scan_enable_port must exist in synth.v as a port. Catch this BEFORE
# OpenROAD does — its error there is opaque.
if ! grep -E "(input|output)[^;]*\\b${SE_PORT}\\b" "${NETLIST}" >/dev/null 2>&1; then
  echo "[DFT PORT MISMATCH] scan_enable_port '${SE_PORT}' not declared in ${NETLIST}" >&2; exit 9
fi

LIB="${SKY130_LIB:-}"

cat > "${TCL}" <<EOF
# Generated by workflow/dft/scripts/write_dft_tcl.sh
# IP: ${IP}   top: ${TOP}   scan_enable: ${SE_PORT}
# max_chains: ${MAX_CHAINS}   max_chain_length: ${MAX_LEN}

read_liberty ${LIB}
read_verilog ${NETLIST}
link_design ${TOP}

set_dft_config -max_length ${MAX_LEN} -max_chains ${MAX_CHAINS} -clock_mixing no_mix
preview_dft -verbose
insert_dft

write_verilog ${OUT}/scan.v
report_dft
exit
EOF
echo "[DFT] wrote ${TCL} (top=${TOP} scan_en=${SE_PORT} max_chains=${MAX_CHAINS} max_len=${MAX_LEN})"
