#!/usr/bin/env bash
# write_sta_tcl.sh — Generate <ip>/sta/run.tcl that drives OpenSTA.
# Args: <ip_name>
set -uo pipefail

IP="${1:-}"
if [ -z "${IP}" ]; then echo "[STA] usage: write_sta_tcl.sh <ip_name>" >&2; exit 2; fi

NETLIST="${IP}/syn/out/synth.v"
SDC="${IP}/sta/out/${IP}.sdc"
TCL="${IP}/sta/run.tcl"
OUT="${IP}/sta/out"
mkdir -p "${IP}/sta" "${OUT}"

if [ ! -s "${NETLIST}" ]; then echo "[STA] missing ${NETLIST}" >&2; exit 5; fi
if [ ! -f "${SDC}"     ]; then echo "[STA] missing ${SDC} — run /sta-sdc first" >&2; exit 2; fi

# Resolve top from SSOT (fallback to IP name).
SSOT="${IP}/yaml/${IP}.ssot.yaml"
TOP=$(grep -E "^[[:space:]]*(top_module|top)[[:space:]]*:" "${SSOT}" 2>/dev/null | head -1 \
  | sed -E 's/^[[:space:]]*(top_module|top)[[:space:]]*:[[:space:]]*//; s/[\"'\'']//g; s/[[:space:]]+$//')
if [ -z "${TOP}" ]; then TOP="${IP}"; fi

cat > "${TCL}" <<EOF
# Generated by workflow/sta/scripts/write_sta_tcl.sh
# IP: ${IP}   top: ${TOP}
read_liberty \$::env(SKY130_LIB)
read_verilog ${NETLIST}
link_design ${TOP}
read_sdc ${SDC}

report_checks -path_delay max -group_count 5 -fields {slew capacitance input_pins nets fanout} > ${OUT}/setup.rpt
report_checks -path_delay min -group_count 5 -fields {slew capacitance input_pins nets fanout} > ${OUT}/hold.rpt
report_checks -path_delay min_max -group_count 10                                                > ${OUT}/timing.rpt
report_wns
report_tns
EOF
echo "[STA] wrote ${TCL} (top=${TOP})"
