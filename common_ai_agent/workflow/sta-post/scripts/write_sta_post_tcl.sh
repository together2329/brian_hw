#!/usr/bin/env bash
# write_sta_post_tcl.sh — Generate <ip>/sta-post/run.tcl with read_spef.
# Args: <ip>
set -uo pipefail

IP="${1:-}"
if [ -z "${IP}" ]; then echo "[STA-POST] usage: write_sta_post_tcl.sh <ip>" >&2; exit 2; fi

ROUTED_V="${IP}/pnr/out/routed.v"
ROUTED_SPEF="${IP}/pnr/out/routed.spef"
SDC="${IP}/sta/out/${IP}.sdc"
TCL="${IP}/sta-post/run.tcl"
OUT="${IP}/sta-post/out"
SSOT="${IP}/yaml/${IP}.ssot.yaml"
mkdir -p "${IP}/sta-post" "${OUT}"

[ ! -s "${ROUTED_V}" ]    && { echo "[STA-POST] missing ${ROUTED_V}"    >&2; exit 5; }
[ ! -s "${ROUTED_SPEF}" ] && { echo "[STA-POST] missing ${ROUTED_SPEF}" >&2; exit 5; }
[ ! -f "${SDC}" ]         && { echo "[STA-POST] missing ${SDC}"         >&2; exit 5; }

TOP=$(grep -E "^[[:space:]]*(top_module|top)[[:space:]]*:" "${SSOT}" 2>/dev/null | head -1 \
  | sed -E 's/^[[:space:]]*(top_module|top)[[:space:]]*:[[:space:]]*//; s/[\"'\'']//g; s/[[:space:]]+$//')
[ -z "${TOP}" ] && TOP="${IP}"

cat > "${TCL}" <<EOF
# Generated by workflow/sta-post/scripts/write_sta_post_tcl.sh
# IP: ${IP}   top: ${TOP}   mode: post_route (parasitic-aware)
read_liberty \$::env(SKY130_LIB)
read_verilog ${ROUTED_V}
link_design ${TOP}
read_sdc ${SDC}
read_spef ${ROUTED_SPEF}

# Functional-mode case analysis when scan ports exist.
if {![catch {get_ports scan_en} _]} {
  set_case_analysis 0 [get_ports scan_en]
}

report_checks -path_delay max -group_count 5 -fields {slew capacitance input_pins nets fanout} > ${OUT}/setup.rpt
report_checks -path_delay min -group_count 5 -fields {slew capacitance input_pins nets fanout} > ${OUT}/hold.rpt
report_checks -path_delay min_max -group_count 10                                                > ${OUT}/timing.rpt
report_clock_skew                                                                                > ${OUT}/skew.rpt
report_wns
report_tns
EOF
echo "[STA-POST] wrote ${TCL} (top=${TOP})"
