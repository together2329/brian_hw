def _load_ssot(ip_root: Path, ip: str) -> dict:
    path = ip_root / "yaml" / f"{ip}.ssot.yaml"
    if not path.exists():
        raise SystemExit(f"[ssot-tb] SSOT not found: {path}")
    with path.open("r", encoding="utf-8") as fh:
        return yaml.safe_load(fh) or {}


def _is_apb_timer(doc: dict) -> bool:
    regs = (doc.get("registers") or {}).get("register_list") or []
    reg_names = {str(r.get("name") or "").upper() for r in regs if isinstance(r, dict)}
    interfaces = (doc.get("io_list") or {}).get("interfaces") or []
    ports = set()
    for intf in interfaces:
        for p in (intf or {}).get("ports") or []:
            if isinstance(p, dict) and p.get("name"):
                ports.add(str(p["name"]))
    return {"CTRL", "COUNT", "COMPARE", "STATUS"}.issubset(reg_names) and {
        "paddr",
        "psel",
        "penable",
        "pwrite",
        "pwdata",
        "pstrb",
        "prdata",
        "pready",
        "pslverr",
        "irq",
    }.issubset(ports)


def _collect_ports(doc: dict) -> set[str]:
    interfaces = (doc.get("io_list") or {}).get("interfaces") or []
    ports = set()
    for intf in interfaces:
        for p in (intf or {}).get("ports") or []:
            if isinstance(p, dict) and p.get("name"):
                ports.add(str(p["name"]))
    return ports


def _is_cpu(doc: dict) -> bool:
    top = doc.get("top_module") or {}
    ports = _collect_ports(doc)
    cpu_ports = {
        "imem_araddr",
        "imem_arvalid",
        "imem_arready",
        "imem_rdata",
        "imem_rvalid",
        "imem_rready",
        "dmem_awaddr",
        "dmem_awvalid",
        "dmem_wdata",
        "dmem_wvalid",
        "dmem_araddr",
        "dmem_arvalid",
        "dbg_pc",
        "dbg_commit_valid",
    }
    return str(top.get("type") or "").lower() == "cpu" and cpu_ports.issubset(ports)


def _is_bus(doc: dict) -> bool:
    top = doc.get("top_module") or {}
    ports = _collect_ports(doc)
    bus_ports = {
        "cpu_i_araddr", "cpu_i_arvalid", "cpu_i_arready", "cpu_i_rdata", "cpu_i_rvalid",
        "cpu_d_awaddr", "cpu_d_awvalid", "cpu_d_wdata", "cpu_d_wvalid", "cpu_d_bvalid",
        "cpu_d_araddr", "cpu_d_arvalid", "cpu_d_rdata", "cpu_d_rvalid",
        "mem_i_araddr", "mem_i_arvalid", "mem_i_rdata", "mem_i_rvalid",
        "mem_d_awaddr", "mem_d_awvalid", "mem_d_wdata", "mem_d_wvalid", "mem_d_bvalid",
        "periph_awaddr", "periph_awvalid", "periph_wdata", "periph_wvalid", "periph_bvalid",
        "route_busy", "route_sel", "route_error",
    }
    return str(top.get("type") or "").lower() == "bus" and bus_ports.issubset(ports)


def _static_coverage(ip_root: Path, ip: str) -> dict:
    rtl_paths = sorted((ip_root / "rtl").glob("*.sv"))
    total_lines = 0
    branch_total = 0
    fsm_total = 0
    for path in rtl_paths:
        for raw in path.read_text(encoding="utf-8", errors="ignore").splitlines():
            line = raw.strip()
            if not line or line.startswith("//"):
                continue
            total_lines += 1
            if line.startswith("if ") or line.startswith("if(") or line.startswith("else") or " case " in f" {line} " or line.startswith("case"):
                branch_total += 1
            if "_state" in line or " state" in line or line.startswith("typedef enum"):
                fsm_total += 1
    return {
        "method": "static RTL universe scan plus cocotb functional smoke bins",
        "note": "Static scan defines line/branch/FSM universe only. It is not simulator-instrumented hit coverage.",
        "lines": {"hit": 0, "total": total_lines, "pct": 0.0, "source": "static_universe_only", "instrumented": False},
        "branches": {"hit": 0, "total": branch_total, "pct": 0.0, "source": "static_universe_only", "instrumented": False},
        "fsm": {"hit": 0, "total": fsm_total, "pct": 0.0, "source": "static_universe_only", "instrumented": False},
        "universe": {"lines": total_lines, "branches": branch_total, "fsm": fsm_total},
        "files": [str(p.relative_to(ip_root.parent)) for p in rtl_paths],
    }


def _write_sim_history(ip_root: Path, ip: str, ok: bool, start: float, cov_pct: float) -> None:
    sim_dir = ip_root / "sim"
    sim_dir.mkdir(parents=True, exist_ok=True)
    path = sim_dir / "history.json"
    try:
        doc = json.loads(path.read_text(encoding="utf-8")) if path.exists() else {}
    except Exception:
        doc = {}
    runs = doc.get("runs") if isinstance(doc.get("runs"), list) else []
    runs.insert(
        0,
        {
            "t": datetime.now().strftime("%H:%M:%S"),
            "tests": "9/9" if ok else "0/9",
            "cov": f"{cov_pct:.0f}%",
            "dur": f"{time.time() - start:.1f}s",
            "status": "ok" if ok else "err",
            "kind": "cocotb",
        },
    )
    path.write_text(json.dumps({"runs": runs[:20]}, indent=2), encoding="utf-8")


def _copy_vcd(ip_root: Path, ip: str) -> Path | None:
    sim_dir = ip_root / "sim"
    candidates = sorted((sim_dir / "cocotb_build").rglob("*.vcd"), key=lambda p: p.stat().st_mtime, reverse=True)
    if not candidates:
        return None
    out = sim_dir / f"{ip}.vcd"
    shutil.copy2(candidates[0], out)
    return out


def _copy_results_xml(ip_root: Path) -> Path | None:
    build_dir = ip_root / "sim" / "cocotb_build"
    candidates = sorted(build_dir.rglob("*_results.xml"), key=lambda p: p.stat().st_mtime, reverse=True)
    if not candidates:
        return None
    out = ip_root / "tb" / "cocotb" / "results.xml"
    out.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(candidates[0], out)
    return out


def _write_toggle_summary(ip_root: Path, vcd_path: Path | None) -> None:
    cov_dir = ip_root / "cov"
    cov_dir.mkdir(parents=True, exist_ok=True)
    if not vcd_path or not vcd_path.exists():
        (cov_dir / "toggle.json").write_text(json.dumps({"signals": {}, "total_toggles": 0}, indent=2), encoding="utf-8")
        return
    id_to_name = {}
    last = {}
    toggles = {}
    scope = []
    for line in vcd_path.read_text(encoding="utf-8", errors="ignore").splitlines():
        parts = line.split()
        if len(parts) >= 3 and parts[0] == "$scope":
            scope.append(parts[2])
        elif len(parts) >= 2 and parts[0] == "$upscope" and scope:
            scope.pop()
        elif len(parts) >= 5 and parts[0] == "$var":
            code = parts[3]
            name = ".".join(scope + [parts[4]])
            id_to_name[code] = name
            toggles.setdefault(name, 0)
        elif line and line[0] in "01xz":
            code = line[1:]
            if code in id_to_name and last.get(code) != line[0]:
                if code in last:
                    toggles[id_to_name[code]] += 1
                last[code] = line[0]
        elif line and line[0] == "b":
            seg = line.split()
            if len(seg) == 2 and seg[1] in id_to_name and last.get(seg[1]) != seg[0]:
                if seg[1] in last:
                    toggles[id_to_name[seg[1]]] += 1
                last[seg[1]] = seg[0]
    top = dict(sorted(toggles.items(), key=lambda item: item[1], reverse=True)[:50])
    (cov_dir / "toggle.json").write_text(
        json.dumps({"signals": top, "total_toggles": sum(toggles.values())}, indent=2),
        encoding="utf-8",
    )


def _write_coverage_report(ip_root: Path, ip: str) -> None:
    cov_path = ip_root / "cov" / "coverage.json"
    toggle_path = ip_root / "cov" / "toggle.json"
    sim_dir = ip_root / "sim"
    sim_dir.mkdir(parents=True, exist_ok=True)
    try:
        cov = json.loads(cov_path.read_text(encoding="utf-8"))
    except Exception:
        cov = {}
    try:
        toggle = json.loads(toggle_path.read_text(encoding="utf-8"))
    except Exception:
        toggle = {}
    lines = [
        f"# {ip} sim_debug coverage",
        "",
        f"status: {cov.get('status', 'unknown')}",
        f"functional: {(cov.get('functional') or {}).get('pct', 0)}%",
        f"line static universe: {(cov.get('lines') or {}).get('total', 0)} lines; instrumented hits unavailable",
        f"branch static universe: {(cov.get('branches') or {}).get('total', 0)} branches; instrumented hits unavailable",
        f"fsm static universe: {(cov.get('fsm') or {}).get('total', 0)} FSM markers; instrumented hits unavailable",
        f"toggle_signals: {len((toggle.get('signals') or {}))}",
        f"total_toggles: {toggle.get('total_toggles', 0)}",
        "",
        "Note: line/branch/FSM entries are static universe counts unless a later instrumented coverage flow provides hit data.",
    ]
    (sim_dir / "coverage_report.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> int:
    if os.environ.get("ATLAS_ALLOW_FIXED_TB_FALLBACK") != "1":
        raise SystemExit(
            "[ssot-tb] fixed fallback generator is disabled. "
            "Use ATLAS /ssot-tb so tb-gen derives a pyuvm/cocotb environment "
            "from the current SSOT and RTL. Set ATLAS_ALLOW_FIXED_TB_FALLBACK=1 "
            "only for explicit legacy migration."
        )

    ap = argparse.ArgumentParser()
    ap.add_argument("ip")
    ap.add_argument("--root", default=".")
    ns = ap.parse_args()

    root = Path(ns.root).resolve()
    ip = ns.ip
    ip_root = root / ip
    doc = _load_ssot(ip_root, ip)
    is_apb = _is_apb_timer(doc)
    is_cpu = _is_cpu(doc)
    is_bus = _is_bus(doc)
    if not (is_apb or is_cpu or is_bus):
        raise SystemExit("[ssot-tb] unsupported SSOT shape for cocotb fallback; expected APB timer, CPU, or AXI-lite bus ports")

    tb_dir = ip_root / "tb" / "cocotb"
    tb_dir.mkdir(parents=True, exist_ok=True)
    test_path = tb_dir / f"test_{ip}.py"
    runner_path = tb_dir / "test_runner.py"
    if is_bus:
        static_cov = _static_coverage(ip_root, ip)
        test_path.write_text(BUS_TEST_TEMPLATE, encoding="utf-8")
        runner_path.write_text(
            BUS_RUNNER_TEMPLATE.format(ip=ip, static_coverage_json=repr(static_cov)),
            encoding="utf-8",
        )
    elif is_cpu:
        static_cov = _static_coverage(ip_root, ip)
        test_path.write_text(CPU_TEST_TEMPLATE, encoding="utf-8")
        runner_path.write_text(
            CPU_RUNNER_TEMPLATE.format(ip=ip, static_coverage_json=repr(static_cov)),
            encoding="utf-8",
        )
    else:
        test_path.write_text(TEST_TEMPLATE, encoding="utf-8")
        runner_path.write_text(RUNNER_TEMPLATE.format(ip=ip), encoding="utf-8")

    start = time.time()
    proc = subprocess.run(
        [sys.executable, "-m", "pytest", "-q", str(runner_path), "--tb=short"],
        cwd=str(ip_root),
        capture_output=True,
        text=True,
        timeout=180,
    )
    output = (proc.stdout or "") + (proc.stderr or "")
    sys.stdout.write(output)

    cov_path = ip_root / "cov" / "coverage.json"
    cov_pct = 0.0
    if cov_path.exists():
        try:
            cov_pct = float((json.loads(cov_path.read_text(encoding="utf-8")).get("functional") or {}).get("pct") or 0.0)
        except Exception:
            cov_pct = 0.0
    ok = proc.returncode == 0
    _write_sim_history(ip_root, ip, ok, start, cov_pct)
    vcd_path = _copy_vcd(ip_root, ip)
    results_path = _copy_results_xml(ip_root)
    _write_toggle_summary(ip_root, vcd_path)
    _write_coverage_report(ip_root, ip)

    print(f"[ssot-tb] cocotb {'PASS' if ok else 'FAIL'}: {ip}")
    print(f"[ssot-tb] coverage: functional {cov_pct:.0f}% -> {ip}/cov/coverage.json")
    if vcd_path:
        print(f"[ssot-tb] sim debug VCD: {vcd_path.relative_to(root)}")
    else:
        print("[ssot-tb] sim debug VCD: not generated by simulator")
    print("[ssot-tb] files:")
    for p in [
        test_path,
        runner_path,
        cov_path,
        ip_root / "cov" / "toggle.json",
        ip_root / "sim" / "history.json",
        ip_root / "sim" / "coverage_report.md",
    ]:
        if p.exists():
            print(str(p.relative_to(root)))
    if results_path and results_path.exists():
        print(str(results_path.relative_to(root)))
    return proc.returncode


if __name__ == "__main__":
    raise SystemExit(main())
