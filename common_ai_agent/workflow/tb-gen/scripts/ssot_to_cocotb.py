@cocotb.test()
async def rv32i_cpu_smoke_and_debug_coverage(dut):
    cocotb.start_soon(Clock(dut.clk, 10, units="ns").start())
    await _reset(dut)

    pc_values = []
    instr_valid_seen = False
    data_valid_seen = False
    commit_seen = False
    trap_or_clean = False
    store_seen = False
    load_seen = False
    memory = {0: 0}

    for _cycle in range(80):
        fetch_addr = _as_int(dut.imem_araddr.value) & 0xFFFF_FFFC
        dut.imem_rdata.value = PROGRAM.get(fetch_addr, NOP)
        read_addr = _as_int(dut.dmem_araddr.value) & 0xFFFF_FFFC
        dut.dmem_rdata.value = memory.get(read_addr, 0)

        if _as_int(dut.dmem_awvalid.value) and _as_int(dut.dmem_wvalid.value):
            write_addr = _as_int(dut.dmem_awaddr.value) & 0xFFFF_FFFC
            memory[write_addr] = _as_int(dut.dmem_wdata.value)
            store_seen = True
        if _as_int(dut.dmem_arvalid.value):
            load_seen = True

        await RisingEdge(dut.clk)
        pc_values.append(_as_int(dut.dbg_pc.value))
        instr_valid_seen = instr_valid_seen or bool(_as_int(dut.imem_arvalid.value)) or bool(_as_int(dut.imem_rready.value))
        data_valid_seen = data_valid_seen or bool(_as_int(dut.dmem_awvalid.value)) or bool(_as_int(dut.dmem_wvalid.value)) or bool(_as_int(dut.dmem_arvalid.value))
        commit_seen = commit_seen or bool(_as_int(dut.dbg_commit_valid.value))
        trap_or_clean = trap_or_clean or bool(_as_int(dut.dbg_trap.value)) or bool(_as_int(dut.dbg_halt.value))
        if _as_int(dut.dbg_halt.value):
            break

    assert len(set(pc_values)) > 1
    _set_bin("debug_pc_changes")
    if instr_valid_seen:
        _set_bin("instruction_axi_activity")
    if data_valid_seen:
        _set_bin("data_axi_activity")
    if commit_seen:
        _set_bin("debug_commit_activity")
    if trap_or_clean or _as_int(dut.dbg_trap.value) == 0:
        _set_bin("trap_halt_observed_or_reset_clean")
    if 0x1C in pc_values and 0x18 not in pc_values:
        _set_bin("rv32i_branch_class_verified")
    if store_seen and load_seen and memory.get(0) == 12:
        _set_bin("rv32i_load_store_class_verified")

