#!/usr/bin/env bash
# run_fp.sh — Floorplan stage. Single OpenROAD invocation.
# Args: <ip>
# Reads: scan.v|synth.v + SDC + LEFs. Writes: <ip>/pnr/out/floorplan.def.
set -uo pipefail

DIR="$(dirname "$0")"
. "${DIR}/_pnr_common.sh"

IP="${1:-}"; [ -z "${IP}" ] && { echo "[PNR] usage: run_fp.sh <ip>" >&2; exit 2; }
[ ! -d "${IP}" ] && { echo "[PNR] no such IP dir: ${IP}" >&2; exit 2; }

pnr_check_tools || exit $?
NETLIST=$(pnr_check_handoff "${IP}") || exit $?
TOP=$(pnr_top_from_ssot "${IP}")
SDC="${IP}/sta/out/${IP}.sdc"
TCL="${IP}/pnr/tcl/floorplan.tcl"
DEF="${IP}/pnr/out/floorplan.def"
LOG="${IP}/pnr/out/pnr.log"
mkdir -p "${IP}/pnr/tcl" "${IP}/pnr/out"

# Pull pnr.* from SSOT for floorplan params.
read UTIL AR CSPACE HOR VER < <(python3 - "${IP}/yaml/${IP}.ssot.yaml" <<'PY'
import sys, pathlib
try:
    import yaml; d = yaml.safe_load(pathlib.Path(sys.argv[1]).read_text()) or {}
except Exception: d = {}
p = d.get("pnr", {}) or {}
io = p.get("io_layers", {}) or {}
print(int(p.get("utilization_pct", 60)),
      float(p.get("aspect_ratio", 1.0)),
      float(p.get("core_space_um", 2.0)),
      io.get("horizontal", "met2"),
      io.get("vertical",   "met3"))
PY
)

cat > "${TCL}" <<EOF
# Generated by workflow/pnr/scripts/run_fp.sh
read_lef ${SKY130_TLEF}
read_lef ${SKY130_LEF}
read_liberty ${SKY130_LIB}
read_verilog ${NETLIST}
link_design ${TOP}
read_sdc ${SDC}

# DFT-aware: tie scan_en low so functional-mode timing drives placement.
if {[llength [info commands get_ports]] && ![catch {get_ports scan_en}]} {
  set_case_analysis 0 [get_ports scan_en]
}

initialize_floorplan -utilization ${UTIL} -aspect_ratio ${AR} \\
  -core_space ${CSPACE} -site unithd

# Routing tracks per metal layer — required by place_pins / global_route.
# ORFS sky130hd platform tracks; falls back to no-op if file absent.
if {[file exists \$::env(SKY130_TRACKS)]} {
  source \$::env(SKY130_TRACKS)
}

place_pins -hor_layers ${HOR} -ver_layers ${VER}
write_def ${DEF}
report_design_area
exit
EOF

echo "[PNR-FP] openroad → ${DEF}  (util=${UTIL}% AR=${AR})"
openroad -no_init -exit "${TCL}" 2>&1 | tee -a "${LOG}"
RC=${PIPESTATUS[0]}
if [ "${RC}" -ne 0 ] || [ ! -s "${DEF}" ]; then
  echo "[PNR-FP] FAILED rc=${RC}" >&2; exit "${RC}"
fi
echo "[PNR-FP HANDOFF] ${DEF} ready — run /pnr-place"
