#!/usr/bin/env bash
# =============================================================================
# run_openroad.sh — OpenROAD floorplan + global placement + STA
# =============================================================================
# Usage:
#   run_openroad.sh <ip> [--root .]
#
# Pipeline (single OpenROAD invocation, all in TCL):
#   1. read_liberty + read_lef (sky130hd typical corner)
#   2. read_verilog (yosys netlist) + link_design <top>
#   3. read_sdc (SSOT-driven SDC)
#   4. initialize_floorplan + make_tracks
#   5. global_placement
#   6. detailed_placement
#   7. report_design_area, report_worst_slack, report_tns
#   8. write_def
#
# Inputs:
#   <ip>/syn/<ip>.netlist.v      from yosys (sky130-mapped)
#   <ip>/sdc/<ip>.sdc            from emit_sdc.py
#
# Outputs:
#   <ip>/pnr/<ip>.def            placed DEF
#   <ip>/pnr/openroad.log        full openroad log
#   <ip>/pnr/pnr_report.txt      summary (area, slack, util)
# =============================================================================
set -uo pipefail

IP=""
ROOT="."
while [[ $# -gt 0 ]]; do
    case "$1" in
        --root) ROOT="$2"; shift 2 ;;
        -*) echo "[run_openroad] unknown flag: $1" >&2; exit 2 ;;
        *) IP="$1"; shift ;;
    esac
done
[[ -z "$IP" ]] && { echo "usage: run_openroad.sh <ip> [--root .]" >&2; exit 2; }
ROOT="$(cd "$ROOT" && pwd)"
IP_DIR="$ROOT/$IP"

PDK="$HOME/src/OpenROAD/test/sky130hd"
LIBERTY="$PDK/sky130_fd_sc_hd__tt_025C_1v80.lib"
TECH_LEF="$PDK/sky130_fd_sc_hd__nom.tlef"
CELLS_LEF="$PDK/sky130_fd_sc_hd_merged.lef"

for f in "$LIBERTY" "$TECH_LEF" "$CELLS_LEF"; do
    [[ -f "$f" ]] || { echo "[run_openroad] missing PDK file: $f" >&2; exit 1; }
done

NETLIST="$IP_DIR/syn/${IP}.netlist.v"
SDC="$IP_DIR/sdc/${IP}.sdc"
[[ -f "$NETLIST" ]] || { echo "[run_openroad] missing netlist: $NETLIST. Run run_synth.sh first." >&2; exit 1; }
[[ -f "$SDC" ]] || { echo "[run_openroad] missing SDC: $SDC. Run emit_sdc.py first." >&2; exit 1; }

# Top module — same heuristic as run_synth.sh
TOP="$(python3 - <<PY 2>/dev/null
import yaml; d=yaml.safe_load(open('$IP_DIR/yaml/$IP.ssot.yaml'))
for sm in d.get('sub_modules') or []:
    if isinstance(sm, dict) and sm.get('wiring_only'):
        nm = (sm.get('file') or '').split('/')[-1].replace('.sv','').replace('.v','')
        if nm: print(nm); raise SystemExit
print((d.get('top_module') or {}).get('name') or '$IP')
PY
)"
[[ -z "$TOP" ]] && TOP="${IP}_wrapper"

PNR_DIR="$IP_DIR/pnr"
mkdir -p "$PNR_DIR"
TCL="$PNR_DIR/openroad_run.tcl"
LOG="$PNR_DIR/openroad.log"
REPORT="$PNR_DIR/pnr_report.txt"
DEF="$PNR_DIR/${IP}.def"

cat > "$TCL" <<EOF
# OpenROAD flow auto-generated by run_openroad.sh
read_liberty $LIBERTY
read_lef $TECH_LEF
read_lef $CELLS_LEF
read_verilog $NETLIST
link_design $TOP
read_sdc $SDC

# ── Floorplan ────────────────────────────────────────────────
# Aspect ratio 1, utilization 50%, sky130hd unit_hd site
initialize_floorplan \\
    -utilization 15 \\
    -aspect_ratio 1.0 \\
    -core_space 20 \\
    -site unithd

source $PDK/sky130hd.tracks

# Routing layers must be declared before place_pins picks legal positions.
set_routing_layers -signal met1-met5

# Pin placement on the die boundary so global_placement can route to them.
puts "─── Pin placement ──────────────────────────────"
place_pins -hor_layers met3 -ver_layers met2

puts "─── Floorplan ──────────────────────────────────"
report_design_area

# ── Power planning (basic strap grid) ────────────────────────
# Skip full PDN for this PoC — OpenROAD pdngen requires a config file.
# Without PDN, routing won't be meaningful, but placement and STA work.

# ── Placement ────────────────────────────────────────────────
puts "─── Global placement ───────────────────────────"
global_placement -density 0.6 -pad_left 1 -pad_right 1

puts "─── Detailed placement ─────────────────────────"
detailed_placement
check_placement -verbose

puts "─── Post-placement timing ──────────────────────"
estimate_parasitics -placement
report_worst_slack -max
report_worst_slack -min
report_tns
report_wns
report_design_area

# Save the placed DEF
write_def $DEF
puts "DEF: placed DEF written to $DEF"

exit
EOF

echo "[run_openroad] PDK: sky130hd typical corner"
echo "[run_openroad] top: $TOP"
echo "[run_openroad] running OpenROAD (floorplan + global + detailed placement + STA)..."

if openroad -no_init -exit "$TCL" 2>&1 | tee "$LOG" | tail -120 > "$REPORT"; then
    echo ""
    echo "[run_openroad] === SUMMARY ==="
    grep -E "Design area|Util|worst slack|tns max|wns max|placement violations" "$LOG" | tail -20
    echo ""
    echo "[run_openroad] OK"
    echo "[run_openroad]   def:    $DEF"
    echo "[run_openroad]   log:    $LOG"
    echo "[run_openroad]   report: $REPORT"
    exit 0
else
    echo "[run_openroad] OpenROAD failed — see $LOG" >&2
    exit 1
fi
