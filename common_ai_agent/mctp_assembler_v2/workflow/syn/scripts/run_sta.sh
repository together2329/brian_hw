#!/usr/bin/env bash
# =============================================================================
# run_sta.sh — OpenSTA timing analysis on the synthesised netlist
# =============================================================================
# Usage:
#   run_sta.sh <ip> [--root .] [--liberty <file>] [--corner tt|ff|ss]
#
# Pipeline:
#   1. yosys synth with sky130 liberty (-> real cell mapping + area).
#   2. OpenSTA: load liberty + netlist + SDC + report worst slack + area.
#
# Outputs:
#   <ip>/syn/<ip>.netlist.v          netlist (sky130-mapped)
#   <ip>/syn/synth_report.txt        yosys area
#   <ip>/syn/sta_report.txt          OpenSTA timing slack + area
# =============================================================================
set -uo pipefail

IP=""
ROOT="."
LIBERTY=""
CORNER="tt"

while [[ $# -gt 0 ]]; do
    case "$1" in
        --root)    ROOT="$2"; shift 2 ;;
        --liberty) LIBERTY="$2"; shift 2 ;;
        --corner)  CORNER="$2"; shift 2 ;;
        -*) echo "[run_sta] unknown flag: $1" >&2; exit 2 ;;
        *) IP="$1"; shift ;;
    esac
done

[[ -z "$IP" ]] && { echo "usage: run_sta.sh <ip> [--root .] [--liberty file]" >&2; exit 2; }
ROOT="$(cd "$ROOT" && pwd)"
IP_DIR="$ROOT/$IP"
SCRIPTS="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
SOURCE_ROOT="$(cd "$SCRIPTS/../../.." && pwd)"

# Default liberty: sky130 typical corner from OpenROAD test PDK if not given.
if [[ -z "$LIBERTY" ]]; then
    for cand in \
        "$HOME/src/OpenROAD/test/sky130hd/sky130_fd_sc_hd__${CORNER}_025C_1v80.lib" \
        "$HOME/src/OpenROAD/test/sky130hd/sky130_fd_sc_hd__tt_025C_1v80.lib" \
        "$SOURCE_ROOT/pdk/sky130/lib/sky130_fd_sc_hd__tt_025C_1v80.lib"; do
        [[ -f "$cand" ]] && LIBERTY="$cand" && break
    done
fi
[[ -z "$LIBERTY" || ! -f "$LIBERTY" ]] && { echo "[run_sta] no liberty file" >&2; exit 1; }

echo "[run_sta] liberty: $LIBERTY"

# ── Stage 1: yosys synth with liberty ────────────────────────────────
echo "[run_sta] (1/2) yosys synth with sky130 mapping..."
bash "$SCRIPTS/run_synth.sh" "$IP" --root "$ROOT" --liberty "$LIBERTY" 2>&1 | tail -15

NETLIST="$IP_DIR/syn/${IP}.netlist.v"
[[ ! -f "$NETLIST" ]] && { echo "[run_sta] netlist missing: $NETLIST" >&2; exit 1; }

# Find top module name (mirrors run_synth.sh logic).
TOP="$(python3 - <<PY 2>/dev/null
import yaml; d=yaml.safe_load(open('$IP_DIR/yaml/$IP.ssot.yaml'))
for sm in d.get('sub_modules') or []:
    if isinstance(sm, dict) and sm.get('wiring_only'):
        nm = (sm.get('file') or '').split('/')[-1].replace('.sv','').replace('.v','')
        if nm: print(nm); raise SystemExit
print((d.get('top_module') or {}).get('name') or '$IP')
PY
)"
[[ -z "$TOP" ]] && TOP="${IP}_wrapper"
echo "[run_sta] top: $TOP"

# ── Stage 2: OpenSTA ─────────────────────────────────────────────────
SDC="$IP_DIR/sdc/${IP}.sdc"
[[ ! -f "$SDC" ]] && { echo "[run_sta] missing SDC: $SDC" >&2; exit 1; }

REPORT="$IP_DIR/syn/sta_report.txt"

cat > "$IP_DIR/syn/sta_run.tcl" <<EOF
# OpenSTA script auto-generated by run_sta.sh
read_liberty $LIBERTY
read_verilog $NETLIST
link_design $TOP
read_sdc $SDC

puts "─── Clocks ──────────────────────────────────────"
report_clock_properties

puts "─── Worst-case timing path (setup) ──────────────"
report_checks -path_delay max

puts "─── Worst-case timing path (hold) ───────────────"
report_checks -path_delay min

puts "─── Slack summary ───────────────────────────────"
report_worst_slack -max
report_worst_slack -min

puts "─── TNS / WNS ───────────────────────────────────"
report_tns
report_wns

puts "─── Cell + area summary ─────────────────────────"
# OpenSTA doesn't have report_design_area; use report_units / power.
report_units
# Cell count and approximate area come from yosys synth_report.txt.

exit
EOF

echo "[run_sta] (2/2) OpenSTA..."
sta -no_init -exit "$IP_DIR/syn/sta_run.tcl" 2>&1 | tee "$REPORT"

echo ""
echo "[run_sta] OK"
echo "[run_sta]   netlist: $NETLIST"
echo "[run_sta]   sta:     $REPORT"
